# рЃЊрЃљрЃљрЃњрЃћрЃюрЃћрЃарЃўрЃарЃћ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌрЃў рЃарЃўрЃфрЃ«рЃЋрЃў 1РђЊрЃЊрЃљрЃю 100-рЃЏрЃЊрЃћ рЃЊрЃљ рЃљрЃфрЃљрЃЊрЃћ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃњрЃљрЃЏрЃЮрЃўрЃфрЃюрЃЮрЃА. рЃДрЃЮрЃЋрЃћрЃџрЃў рЃЏрЃфрЃЊрЃћрЃџрЃЮрЃЉрЃўрЃАрЃљрЃА рЃЏрЃўрЃБрЃЌрЃўрЃЌрЃћ: "рЃЏрЃћрЃбрЃўрЃљ" рЃЌрЃБ "рЃюрЃљрЃЎрЃџрЃћрЃЉрЃўрЃљ".
import random

def guess_the_number():
    secret_number = random.randint(1, 100)  # рЃЋрЃљрЃњрЃћрЃюрЃћрЃарЃўрЃарЃћрЃЉрЃЌ рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃўрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃА
    attempts = 0

    print("­Ъј» рЃњрЃљрЃЏрЃЮрЃўрЃфрЃљрЃюрЃў рЃарЃўрЃфрЃ«рЃЋрЃў 1-рЃЊрЃљрЃю 100-рЃЏрЃЊрЃћ!")

    while True:
        guess = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃерЃћрЃюрЃў рЃЋрЃљрЃарЃљрЃБрЃЊрЃў: "))
        attempts += 1

        if guess < secret_number:
            print("­Ъћ╝ рЃЏрЃћрЃбрЃўрЃљ!")
        elif guess > secret_number:
            print("­Ъћй рЃюрЃљрЃЎрЃџрЃћрЃЉрЃўрЃљ!")
        else:
            print(f"­ЪјЅ рЃАрЃгрЃЮрЃарЃўрЃљ! рЃарЃўрЃфрЃ«рЃЋрЃў рЃўрЃДрЃЮ {secret_number}.")
            print(f"рЃерЃћрЃю рЃњрЃљрЃЏрЃЮрЃўрЃфрЃљрЃюрЃў {attempts} рЃЏрЃфрЃЊрЃћрЃџрЃЮрЃЉрЃўрЃЌ!")
            break

guess_the_number()
