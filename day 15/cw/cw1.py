#```შექმენით ფუნქცია, რომელსაც გადაეცემა შემდეგი ლისთი: 
#[1,23,165,2,3,92,10,34,911] და ერთი ინტეჯერი(მაგ. 3), თქვენი დავალებაა, 
#რომ ახალ ცარიელ ლისთში შეინხოთ მხოლოდ ისეთი რიცხვები,
# რომლებიც უნაშთოთ იყოფიან გადმოცემულ ინტეჯერზე(ამ შემთხვევაში 3-ზე).```
def flt(list, divider):
    result = [num for num in list if num % divider == 0]
    return result

numbers = [1, 23, 165, 2, 3, 92, 10, 34, 911]
divider = 3
res = flt(numbers, divider)

print(res)
