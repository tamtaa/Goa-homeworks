// 1.
// for loop — ეს არის ციკლი, რომელიც იმეორებს მოქმედებას რამდენჯერმე.
// მას აქვს სამი ნაწილი:
// 1) ინიციალიზაცია (a) — სრულდება მხოლოდ ერთხელ, სანამ ციკლი დაიწყება
// 2) პირობა (b) — მოწმდება ყოველ ჯერზე, სანამ ციკლი გრძელდება
// 3) (c) — სრულდება ყოველი იტერაციის შემდეგ
// მაგალითი:
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// ახსნა:
// a → let i = 0  (მხოლოდ ერთხელ სრულდება)
// b → i < 5      (ამოწმებს, გავაგრძელოთ თუ არა ციკლი)
// c → i++        (ზრდის i-ს ყოველი იტერაციის შემდეგ)



// 2) ახსენით როგორ ვქმნით for loop-ს
// სტრუქტურა:
// for ციკლის შექმნა:
// ფორმა: for (ინიციალიზაცია; პირობა; ცვლილება) { მოქმედება }

for (let i = 0; i < 5; i++) {
  console.log(i);
}

// ახსნა:
// a = let i = 0 — მხოლოდ ერთხელ სრულდება (ვქმნით ცვლადს i)
// b = i < 5 — ამოწმებს, გავაგრძელოთ თუ არა ციკლი
// c = i++ — ზრდის i-ს ყოველ ჯერზე 1-ით



// 3) გამოიყენე for loop, რომ დაბეჭდო რიცხვები 1–დან 5-მდე
for (let i = 1; i <= 5; i++) {
  console.log(i);
}


// 4) გამოიყენე for loop, რომ დაბეჭდო რიცხვები 10–დან 1-მდე
for (let i = 10; i >= 1; i--) {
  console.log(i);
}


// 5) შექმენით arr, გადაუარეთ ამ arr-ს for loop-ით და დაბეჭდეთ თითოეული ელემენტი
let arr = [5, 10, 15, 20];
for (let i = 0; i < arr.length; i++) {
  console.log(arr[i]);
}


// 6) მასივში ['a', 'b', 'c', 'd'] თითოეული ელემენტი გადააქციო დიდ ასოებად და დაბეჭდო
let letters = ['a', 'b', 'c', 'd'];
for (let i = 0; i < letters.length; i++) {
  console.log(letters[i].toUpperCase());
}


// 7) დაბეჭდო ყველა ლუწი რიცხვი 1–დან 20-მდე
for (let i = 1; i <= 20; i++) {
  if (i % 2 === 0) {
    console.log(i);
  }
}


// 8) შექმენი მასივი [3, 7, 10, 15, 20] და დაბეჭდო მხოლოდ ის რიცხვები, რომლებიც 10-ზე მეტია
let nums = [3, 7, 10, 15, 20];
for (let i = 0; i < nums.length; i++) {
  if (nums[i] > 10) {
    console.log(nums[i]);
  }
}


// 9) გამოიყენე for loop, რომ 1–დან 10-მდე ყველა რიცხვის ჯამი გამოითვალო და დაბეჭდე
let sum = 0;
for (let i = 1; i <= 10; i++) {
  sum += i;
}
console.log("ჯამი =", sum);


// 10) FizzBuzz — დაბეჭდე ყველა რიცხვი 1–დან 50-მდე,
// მაგრამ 3-ის ჯერადების ნაცვლად "Fizz",
// 5-ის ჯერადების ნაცვლად "Buzz",
// ხოლო ორივეს ერთად "FizzBuzz"
for (let i = 1; i <= 50; i++) {
  if (i % 3 === 0 && i % 5 === 0) {
    console.log("FizzBuzz");
  } else if (i % 3 === 0) {
    console.log("Fizz");
  } else if (i % 5 === 0) {
    console.log("Buzz");
  } else {
    console.log(i);
  }
}
